#include <stddef.h>

void	*ft_memset(void	*b, int c, size_t len)
{
	size_t			a;
	unsigned char	*v;

	a = 0;
	v = b;
	while (a < len)
	{
		v[a] = c;
		a++;
	}
	b = v;
	return (b);
}
